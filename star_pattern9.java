import java.io.*;
import java.util.*;
class star_pattern9
    {
        public void main()
        
        {
            int n=7;
            for(int i =1;i<=n;i++)
            {
            for(int j=1;j<=n-i+1;j++)
            {
                System.out.print("*");
            }
            System.out.println(" ");
        }
    }
}
 